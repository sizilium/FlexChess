#ifndef REFRESHABLE_HPP_
#define REFRESHABLE_HPP_

// includes
#include <vs/Build.hpp>

namespace vs
{

namespace base
{

namespace interfaces
{

/**
 * Basic interface for all classes whose objects are meant to be
 * refreshed periodically. For classes that support animation or
 * other time dependent tasks use the Updateable interface (@see Updateable). 
 */
class VS_EXPORT Refreshable
{
	/**
	 * Refreshes the object.
	 */
	virtual void refresh() = 0;
};

}

}

}

#endif /*REFRESHABLE_HPP_*/
